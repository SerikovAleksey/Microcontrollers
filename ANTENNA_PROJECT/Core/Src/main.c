// function checks broken message
_Bool check_broken_message(uint8_t* array)
{
	uint16_t first = (array[0] | (array[1]<<8) | (array[2]<<16) | (array[3]<<24));
	uint16_t second = (array[4] | (array[5]<<8) | (array[6]<<16) | (array[7]<<24));
	return ((uint16_t)((first + second)) == (array[8] | (array[9]<<8) | (array[10]<<16) | (array[11]<<24)));
}

// callback function
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) 
	{
	if (check_broken_message(buf) && (buf[8] | (buf[9]<<8) | (buf[10]<<16) | (buf[11]<<24)) == 510) 
		{
			alarm_button();
		}
	if (check_broken_message(buf) && (buf[8] | (buf[9]<<8) | (buf[10]<<16) | (buf[11]<<24)) == 240 && (buf[4] | (buf[5]<<8) | (buf[6]<<16) | (buf[7]<<24)) == 240)
		{
			continue_working();
		}
	if (!check_broken_message(buf))
	{
		//uint8_t array[1] = {0};
		//HAL_UART_Transmit(&huart4, array, 1, 10);
	}
	if ((buf[8] | (buf[9]<<8) | (buf[10]<<16) | (buf[11]<<24)) == 4000)
	{
		check_message = 2;
	}
	
	if ((buf[8] | (buf[9]<<8) | (buf[10]<<16) | (buf[11]<<24)) == 3000)
	{
		check_message = 3;
	}
	if ((buf[0] | (buf[1]<<8) | (buf[2]<<16) | (buf[3]<<24)) <= 360 && (buf[4] | (buf[5]<<8) | (buf[6]<<16) | (buf[7]<<24)) <= 90)
	{
		check_message = 1;
	}
	if ((buf[8] | (buf[9]<<8) | (buf[10]<<16) | (buf[11]<<24)) == 255 && (buf[4] | (buf[5]<<8) | (buf[6]<<16) | (buf[7]<<24)) == 255)
	{
		check_message = 4;
	}
	
  HAL_UART_Receive_IT (&huart4, buf, 12);
	}
